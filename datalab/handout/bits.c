/* 
 * dividePower2 - Compute x/(2^n), for 0 <= n <= 30
 *  Round toward zero
 *   Examples: dividePower2(15,1) = 7, dividePower2(-33,4) = -2
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int dividePower2(int x, int n) {
  /*This method calculates the bias necessary for the division and implements
  it before shifting n bits right*/
    int leadingBit = x >> 31;
    int bias = ((1 << n) + ~0) & leadingBit;
    x = x + bias;
    return x >> n;
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /*This method shifts x n-1 times right and determines if there are all ones
  for a positive number to the left or all ones for a negative number*/
  int shiftedBits = x >> (n + ~0);
  return (!(shiftedBits+1)) | (!shiftedBits);
}
